let dia = 1
switch (dia){
  case 1:
  // executado se for igual a "domingo"
    console.log("hoje é domingo")
    break;
  case 2:
  // executado se for igual a "segunda"
    console.log("hoje é segunda")
    break;
  case 3:
  // executado se for igual a "terça"
    console.log("hoje é terça")
    break;
  case 4:
  // executado se for igual a "quarta"
    console.log("hoje é quarta")
    break;
  case 5:
  // executado se for igual a "quinta"
    console.log("hoje é quinta")
    break;
  case 6:
  // executado se for igual a "sexta"
    console.log("hoje é sexta")
    break;
  case 7:
  // executado se for igual a "sábado"
    console.log("hoje é sábado")
    break;
  default:
   // executado se não for nenhum dos casos acima 
    console.log("hoje é outro dia da semana")
    
}
